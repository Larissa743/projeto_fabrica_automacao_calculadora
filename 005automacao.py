# Importar o pyautogui 
import pyautogui

# "Press" é um comando que utilizamos para pressionar a tecla desejada
pyautogui.press('Win')

# "Sleep" é um comando que utilizamos para deixar o código em espera por alguns segundos
pyautogui.sleep(1)

# "write" é um comando para passar oq queremos escrever
pyautogui.write("Google Chrome")

pyautogui.press("Enter")